//подключаем модуль fs, предназначенный для работы с файловой системой
const fs = require('fs'); 
// Объявляем функцию для чтения текстовых файлов из директории
function listTxtFiles(directory) {
    try {
        const allFiles = fs.readdirSync(directory);// Получаем список файлов в указанной директории
        // Применяем фильтр к списку файлов, выбирая только .txt-файлы
        const txtFiles = allFiles.filter(file => file.endsWith('.txt'));
        return txtFiles;// Возвращаем массив с именами текстовых файлов
    } catch(err) {
        // В случае возникновения ошибки при работе с файловой системой выводим сообщение
        console.error("Ошибка при получении списка файлов:", err.message);
        return null;// Возвращаем значение null, обозначающее неудачу
    }
}
// Основной вызов функции
const result = listTxtFiles('/Users/Светлячок/Pictures/Node'); // Указываем путь к директории
// Проверяем, успешно ли завершился вызов функции и были ли найдены текстовые файлы
if(result !== null && result.length > 0){
    // Если найдено хотя бы одно текстовое название файла, выводим заголовок
    console.log("Список текстовых файлов:");
    // Перебираем массив найденных файлов и выводим их номера и имена
    result.forEach((file, index) => {
        console.log(`${index + 1}. ${file}`); // Выводим номер файла и его имя
    });
} else {
    // Если не было найдено ни одного текстового файла, выводим соответствующее сообщение
    console.log("Нет текстовых файлов в указанной директории.");
}